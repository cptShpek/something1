export const methods = {

    showInfoButton () {

        this.querySelector( '.showMore' ).style.display = 'block';

    },

    hideInfoButton () {

        this.querySelector( '.showMore' ).style.display = 'none';

    },

    showInfoTab () {

        document.querySelector( '.infoTab' ).style.display = 'block';

    },

    hideInfoTab () {

        document.querySelector( '.infoTab' ).style.display = 'none';

    },

}